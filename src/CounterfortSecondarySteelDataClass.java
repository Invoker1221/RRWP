public class CounterfortSecondarySteelDataClass {
private double noOfSecondaryBars;
    private double horizontalForce;
    private double areaOfSteel;
    private double spacingAdopted;
    private double wallHeight;

    public double getNoOfSecondaryBars() {
        return noOfSecondaryBars;
    }

    public void setNoOfSecondaryBars(double noOfSecondaryBars) {
        this.noOfSecondaryBars = noOfSecondaryBars;
    }

    public double getHorizontalForce() {
        return horizontalForce;
    }

    public void setHorizontalForce(double horizontalForce) {
        this.horizontalForce = horizontalForce;
    }

    public double getAreaOfSteel() {
        return areaOfSteel;
    }

    public void setAreaOfSteel(double areaOfSteel) {
        this.areaOfSteel = areaOfSteel;
    }

    public double getSpacingAdopted() {
        return spacingAdopted;
    }

    public void setSpacingAdopted(double spacingAdopted) {
        this.spacingAdopted = spacingAdopted;
    }

    public double getWallHeight() {
        return wallHeight;
    }

    public void setWallHeight(double wallHeight) {
        this.wallHeight = wallHeight;
    }
}
